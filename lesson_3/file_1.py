def main():
    """
    Основная функция для вычисления суммы, разности и произведения двух чисел.
    """
    # Вводим значения для a и b
    a = 5
    b = 3

    # Вычисляем сумму, разность и произведение
    sum_ab = a + b
    diff_ab = a - b
    prod_ab = a * b

    # Выводим результаты
    print("Сумма:", sum_ab)
    print("Разность:", diff_ab)
    print("Произведение:", prod_ab)


if __name__ == "__main__":
    main()

