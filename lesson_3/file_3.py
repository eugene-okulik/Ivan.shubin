import math

# Вводим значения для двух чисел
a = 5
b = 3

# Вычисляем среднее арифметическое
mean_arithmetic = (a + b) / 2

# Вычисляем среднее геометрическое
mean_geometric = math.sqrt(a * b)

# Выводим результаты
print("Среднее арифметическое:", mean_arithmetic)
print("Среднее геометрическое:", mean_geometric)

  